//Incrementing - increases the value by 1
//++    post increment -value is increased after addition
var a=3;
var b=2;
var c=a+b;
var d=15;
d= d++;
console.log(c+  c++); //console.log(5+  5)
console.log(d);

//pre increment- value is increased first and then addition is done
var a=7;
var b=6;
var c=a+b;
var d=24;
d=++d;
console.log(c+  ++c); // console.log (13+  14)
console.log(d);

//post decrement - decreased after the calculation
var a=15;
a=a--;
console.log(a);

// predecrement - decreased before calculating
var a=20;
a= --a;
console.log(a);
